    is_owner: bool,
    is_primary_owner: bool,
    is_restricted: bool,
    is_ultra_restricted: bool,
    is_bot: bool,
    is_app_user: bool,
    updated: i64,
    is_email_confirmed: bool,
    who_can_share_contact_card: []const u8,
};

pub const UserResponse = struct {
    ok: bool,
